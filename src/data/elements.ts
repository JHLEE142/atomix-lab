type RawElementRecord = (typeof rawElements)[number];

export const elements: ElementRecord[] = rawElements.map((element) => {
  const radiusPm = normalizeRadiusPm(element);

  return {
    ...element,
    radiusPm,
    radiusScale: getRadiusScale(radiusPm),
    shells: getElectronShellsFromConfiguration(element.electronConfiguration),
    neutrons: Math.max(0, Math.round(element.atomicMass) - element.atomicNumber)
  };
});

export const elementBySymbol = new Map(elements.map((element) => [element.symbol, element]));

export const labPalette = ["H", "C", "N", "O", "S", "Cl", "Na"];

export function getElement(symbol: string): ElementRecord {
  const element = elementBySymbol.get(symbol);
  if (!element) {
    throw new Error(`Unknown element symbol: ${symbol}`);
  }
  return element;
}

function normalizeRadiusPm(element: RawElementRecord): number {
  if (element.radiusPm > 0) return element.radiusPm;

  const categoryFallbacks: Record<ElementCategory, number> = {
    "nonmetal": 165,
    "noble-gas": 220,
    "alkali-metal": 310,
    "alkaline-earth-metal": 260,
    "metalloid": 205,
    "halogen": 190,
    "post-transition-metal": 210,
    "transition-metal": 215,
    "lanthanide": 235,
    "actinide": 245
  };

  return categoryFallbacks[element.category];
}

function getRadiusScale(radiusPm: number): number {
  return Math.min(100, Math.max(6, Math.round((radiusPm / 350) * 100)));
}
